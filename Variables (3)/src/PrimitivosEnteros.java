public class PrimitivosEnteros {

    public static void main(String[] args) {
        
        byte numeroByte = 127;

        System.out.println(numeroByte);

        System.out.println("tipo Byte corresponde en Byte a: " + Byte.BYTES);
        System.out.println("tipo Byte corresponde en bits a: " + Byte.SIZE);
        System.out.println("tipo Byte corresponde en bits a: " + Byte.MAX_VALUE);
        System.out.println("tipo Byte corresponde en bits a: " + Byte.MIN_VALUE);

        short numeroShort = 32767;

        System.out.println(numeroShort);

        System.out.println("tipo short corresponde en Byte a: " + Short.BYTES);
        System.out.println("tipo short corresponde en bits a: " + Short.SIZE);
        System.out.println("tipo short corresponde en bits a: " + Short.MAX_VALUE);
        System.out.println("tipo short corresponde en bits a: " + Short.MIN_VALUE);

        int numeroInt = 2147483647;

        System.out.println(numeroInt);

        System.out.println("tipo int corresponde en Byte a: " + Integer.BYTES);
        System.out.println("tipo int corresponde en bits a: " + Integer.SIZE);
        System.out.println("tipo int corresponde en bits a: " + Integer.MAX_VALUE);
        System.out.println("tipo int corresponde en bits a: " + Integer.MIN_VALUE);

        long numeroLong = 9223372036854775807L;

        System.out.println(numeroLong);

        System.out.println("tipo long corresponde en Byte a: " + Long.BYTES);
        System.out.println("tipo long corresponde en bits a: " + Long.SIZE);
        System.out.println("tipo long corresponde en bits a: " + Long.MAX_VALUE);
        System.out.println("tipo long corresponde en bits a: " + Long.MIN_VALUE);

        var numeroVar = 2147483647;

        System.out.println(numeroVar);


    }
}
